<script lang="ts">
    import ShareIcon from "$lib/components/icons/ShareIcon.svelte";
    import DialogModal from "$lib/components/modals/DialogModal.svelte";
    import Button from "$lib/components/Button.svelte";

    type ConfigValue = string | string[];

    interface Props {
        parsedConfig: Record<string, ConfigValue> | null;
        previewText: string | null;
        parseError: boolean;
        onclose?: () => void;
        onimport?: () => void;
        keyFormatter?: (key: string) => string;
    }

    const {
        parsedConfig,
        previewText,
        parseError,
        onclose,
        onimport,
        keyFormatter = (key: string) => key
    }: Props = $props();
</script>

<DialogModal title="Shared Config" {onclose}>
    {#snippet icon()}
        <ShareIcon />
    {/snippet}

    <p class="share-modal-desc">Someone shared a Ghostty config with you. Review it before importing.</p>
    <div class="share-preview">
        {#if parsedConfig}
            <div class="row p2"># Config generated by Ghostty Config</div>
            <div class="row">&nbsp;</div>
            {#each Object.entries(parsedConfig) as [key, value], i (i)}
                {#if Array.isArray(value)}
                    {#each value as val, v (v)}
                        {#if val !== ""}
                            <div class="row"><span class="p4">{keyFormatter(key)}</span> = <span class="p5">{val}</span></div>
                        {/if}
                    {/each}
                {:else}
                    <div class="row"><span class="p4">{keyFormatter(key)}</span> = <span class="p5">{value}</span></div>
                {/if}
            {/each}
        {:else}
            {#if parseError}
                <div class="row p2"># Could not parse config structure. Showing raw text:</div>
                <div class="row">&nbsp;</div>
            {/if}
            {#each (previewText ?? "").split("\n") as line, i (i)}
                <div class="row">{line}</div>
            {/each}
        {/if}
    </div>

    {#snippet footer()}
        <Button onclick={onclose}>Dismiss</Button>
        <Button primary onclick={onimport}>Import Config</Button>
    {/snippet}
</DialogModal>

